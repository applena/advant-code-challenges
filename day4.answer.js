function guardSleep(data){
  let guards = {
    // 99: {
    //   totalSlept: 47,
    //   minute: 23
    // }
  }
  // console.log(sortedDataByGuard);
  for (let i = 0; i < data.length; i++){
    let record = data[i];
    let guardId = record[0];
    if(!guards[guardId]){
      guards[guardId] = {
        totalSlept: 0,
        minutes: {}
      }
    }
    let guardObj = guards[guardId];
    //console.log('guard in the while loop is ', guard)
    if(record[6]){ // sleeping
      //console.log('asleep at index ', j)
      let startMinute = record[5];
      // note: if the guard is asleep at the end of the shift--this next record will be the next guard and this might be wrong
      let endMinute = data[i+1][5];
      let timeSlept = endMinute-startMinute;
      guardObj.totalSlept += timeSlept;
      // console.log({guardId, startMinute, endMinute, timeSlept, total:guardObj.totalSlept })
      // figure out the most common minute being asleep
      for (let j = startMinute; j < endMinute; j++){
        if (!guardObj.minutes[j]){
          guardObj.minutes[j] = 1;
        } else {
          guardObj.minutes[j]++;
        }
      }
      //console.log('totalMinutes = ', totalMinutes);
    }
  }
  let mostMinutesSlept = 0;
  let sleepyGuard;
  let mostCommonMinuteCount = 0; // how many times we saw the minute (2)
  let mostCommonMinute = 0; // the minute he slept (24)

  for (let id in guards){
    if (guards[id].totalSlept > mostMinutesSlept){
      mostMinutesSlept = guards[id].totalSlept;
      sleepyGuard = id;
    }
  }
  console.log(mostMinutesSlept, sleepyGuard)
  //console.log('GUARDS ARR ', guards);

  for (let minute in guards[sleepyGuard].minutes){
    let count = guards[sleepyGuard].minutes[minute]
    if (count > mostCommonMinuteCount){
      mostCommonMinuteCount = count;
      mostCommonMinute = minute;
    }
  }
  
  return mostCommonMinute * sleepyGuard;
  
}

